#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*nl;

	nl = 0;
	if (!lst || !f || !del)
		return (NULL);
	while (lst)
	{
		new = ft_lstnew(f(lst->content));
		if (!new)
		{
			ft_lstclear(&nl, del);
			return (nl);
		}
		ft_lstadd_back(&nl, new);
		lst = lst->next;
	}
	return (nl);
}
